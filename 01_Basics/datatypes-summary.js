// Datatypes are divided in two different parts according to how they are stored and accessed in memory.

// 1. PRIMITIVE DATATYPES
// 2. NON-PRIMITIVE DATATYPES (Reference Type).

// PRIMITIVE DATATYPES : They are Call By Value - means whenever they are used somewhere they are not sent as reference of memory, they are always sent as copied
// value of original so that original values gets intact in memory.
// TOTAL: 7
// String, Number, Boolean, null, undefined, Symbol, BigInt

// Non-Primitive Datatype: These reference type and are call by reference - mean wherever they are used they are sent as memory reference and changes made from anywhere will
// be reflected in original data as well.
// Total: 3
// Array, Objects, functions.

// Statically Typed Languages
// Definition:
// The type of a variable is defined at compile time and cannot be changed later. Developers must explicitly declare the type of each variable,
// and type-related errors are detected during compilation.

// Dynamically Typed Languages
// Definition: The type of a variable is determined at runtime, and a variable's type can change during execution.

// Is Javascript dynamic type language or static type language?
// JavaScript is a dynamically typed language, meaning variables are not explicitly tied to a specific data type. Instead, the type of a variable is determined
// dynamically at runtime based on the value assigned to it.

